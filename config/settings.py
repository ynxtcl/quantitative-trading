# ==================== 数据参数 ====================
DATA_CONFIG = {
    "cache_enabled": True,              # 启用本地缓存
    # 为什么必须缓存？akshare 每次调用是网络请求，可能有500ms~3s延迟
    # 5只股票5年数据一次性下载仅需5-15秒，但如果每次都重新下载
    # 调试策略时需要反复运行，时间浪费在等待网络而不是分析结果
    "cache_format": "parquet",          # 缓存格式: parquet / csv
    # parquet 比 csv 优势：
    #   1. 列式存储，读取速度快3-10倍
    #   2. 自动保留数据类型（日期、浮点数精度）
    #   3. 压缩比高（同数据 parquet 约 csv 的1/3大小）
    #   4. 支持按列读取（未来数据量大时只需要读 close 列）
    "max_history_years": 5,             # 最多下载5年数据
    # 5年是 akshare 免费数据的稳定区间
    # 超过5年可能返回空或错误数据
    "freq_check": True,                 # 频率校验，拒绝非日频数据
}

